#include "libft.h"

int		ft_int_len(int n)
{
	int		i;

	i = 0;
	if (n < 0)
	{
		i = 1;
		n = -n;
	}
	if (n == 0)
		return (0);
	while (n > 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
